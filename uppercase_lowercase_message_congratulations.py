# This code converts a congratulation message to uppercase
message = "congratulations"
uppercase_message = message.upper()
print(uppercase_message)

# This code converts a congratulation letter "c" to uppercase
message_2 = "ongratulations"
letter_message = "c"
update_c = letter_message.upper()
print(update_c + message_2)

# This code converts a congratulation letter "C" to lowercase
message_3 = "ongratulations"
letter_message = "C"
update_letter_c = letter_message.lower()
print(update_letter_c + message_3)
